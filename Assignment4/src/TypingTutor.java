    private void boldOptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boldOptionActionPerformed
        // TODO add your handling code here
        
        if(italicOption.isSelected() && boldOption.isSelected()){
            screen.setFont(font.deriveFont(Font.BOLD + Font.ITALIC));
        }
        else if(!italicOption.isSelected() && boldOption.isSelected()) {
            screen.setFont(font.deriveFont(Font.BOLD));
        }
        else if(italicOption.isSelected() &&! boldOption.isSelected()){
            screen.setFont(font.deriveFont(Font.ITALIC));  
        }                 
        else{
            screen.setFont(font.deriveFont(Font.PLAIN));
        }
    }//GEN-LAST:event_boldOptionActionPerformed

